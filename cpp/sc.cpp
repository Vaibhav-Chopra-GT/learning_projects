#include<iostream>

using namespace std;

int main () {

    int i;
    cin >> i;

    switch (i) {
        case 1: {
            cout << "i is 1";
            break;
        }
        case 2: {
            cout << "i is 2";
            break;
        }
        case 3: {
            cout << "i is 3";
            break;
        }
        case 4: {
            cout << "i is 4";
            break;
        }
        default: {
            cout << " ";
        }
    }

    return 0;
}